package jp.andpad.api.domain

/**
 * 組織内メンバーの権限ロールを表す列挙型。
 *
 * ANDPAD 建設 PM プラットフォームにおけるアクセス制御の基本単位。
 * 機能の閲覧・編集・管理権限をロールごとに付与する。
 */
enum class MemberRole {
    /** オーナー：組織の最高権限。課金・メンバー管理・全機能へのアクセス */
    OWNER,

    /** 管理者：メンバー管理・設定変更が可能。オーナー以外の管理業務を担当 */
    ADMIN,

    /** メンバー：通常の現場業務・編集操作が可能 */
    MEMBER,

    /** 閲覧者：読み取り専用。レポート閲覧や進捗確認のみ可能 */
    VIEWER
}
